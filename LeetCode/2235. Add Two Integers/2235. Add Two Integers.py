class Solution(object):
    def sum(self, num1, num2):
        """
        :type num1: int
        :type num2: int
        :rtype: int
        """
        result = num1 + num2
        return result

solution = Solution()
num1 = 12
num2 = 15
result_test = solution.sum(num1, num2)
print(result_test)
